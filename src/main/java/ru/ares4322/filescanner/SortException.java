public class SortException extends Exception{

	public SortException() {
	}

	public SortException(String message) {
		super(message);
	}

	public SortException(Throwable cause) {
		super(cause);
	}

	public SortException(String message, Throwable cause) {
		super(message, cause);
	}

	

}
